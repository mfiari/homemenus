<?php

class Model_Option_Value extends Model_Template {
	
	private $id;
	private $nom;
	
	public function __construct() {
	}
	
	public function __get($property) {
		if (property_exists($this, $property)) {
			return $this->$property;
		}
	}
	
	public function __set($property, $value) {
		if (property_exists($this, $property)) {
			$this->$property = $value;
		}
		return $this;
	}
}